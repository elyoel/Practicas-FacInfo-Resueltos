ID_F10 equ 15
INT0 equ 24h
EOI equ 20h
IMR equ 21h

ORG 1000h
  SELEC db ?
ORG 60
  dw RUT_F10
ORG 3000h
RUT_F10:
  PUSH BX
  PUSH AX
  MOV AL, 1
  MOV BX, OFFSET SELEC
  INT 7
  POP AX
  POP BX
  MOV AL, EOI
  OUT EOI, AL
  INT 0
IRET
ORG 2000h
  CLI
  MOV AL, 0FEh
  OUT IMR, AL
  MOV AL, ID_F10
  OUT INT0, AL
  STI
  
  MOV CL, 30h
  MOV SELEC, CL
UP: INC CL
  MOV SELEC, CL
  CMP CL, 39h
  JNZ UP
DOWN:  DEC CL
  MOV SELEC, CL
  CMP CL, 30h
  JNZ DOWN
  JMP UP
  
  INT 0
  END
  